package day02_println_practice;

public class TellMeAboutYourself {
    public static void main(String[] args){
        System.out.println("My name is Mohammad");
        System.out.println("I am a test automation engineer");
        System.out.println("I specialize in java programming");
        System.out.println("Hello World");
        System.out.println("Hello\nworld");
        System.out.println("Hello\'world");
        System.out.println("Hello\"world");
        System.out.println("Hello\\World");
    }
}
