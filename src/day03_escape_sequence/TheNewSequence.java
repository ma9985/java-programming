package day03_escape_sequence;

public class TheNewSequence {
    public static void main(String[] args){
        System.out.println("I like to \"eat\" cake a lot");
        System.out.println("Lets buy some \"coffee\" and \"tea.\"");
        System.out.println("\\");
        System.out.println("I like to eat pizza and fries.\nItalian beef with some bad ass fries.");
        System.out.println("\tmohammad\tidris\tjinan");
    }
}
