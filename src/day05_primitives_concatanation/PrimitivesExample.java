package day05_primitives_concatanation;

public class PrimitivesExample {
    public static void main(String[] args){
        //*******WHOLE NUMBERS/INTEGERS TYPES********
        byte byteValue = 100;
        short shortValue = 32000;
        int intValue = 987654321;
        long longValue = 987654321L;
        System.out.println(byteValue);
        System.out.println(shortValue);
        System.out.println(intValue);
        System.out.println(longValue);
        //******DECIMAL NUMBERS/FLOATING POINT TYPES***
        float floatValue = 1224.55F;
        double doubleValue = 23564.128846; // can put D at end, optional
        System.out.println(floatValue);
        System.out.println(doubleValue);
        //******CHARACTER TYPE*******
        char charValue = 'b'; //char is a single character and single quotes like male or female.
        System.out.println(charValue);

        //****BOOLEAN  true - false *******
        boolean booleanValue = false; //no double quotes
        System.out.println(booleanValue);
    }
}
